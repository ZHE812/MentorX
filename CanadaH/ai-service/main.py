@app.websocket("/ws/session")
async def websocket_session(websocket: WebSocket):
    await websocket.accept()
    print("[/ws/session] Client connected")

    frames         = []
    session_active = False

    try:
        while True:
            data = await websocket.receive_text()

            try:
                msg      = json.loads(data)
                msg_type = msg.get("type")

                if msg_type == "start":
                    frames         = []
                    session_active = True
                    print("[/ws/session] Session started")
                    await websocket.send_json({"status": "recording"})

                elif msg_type == "stop":
                    session_active = False
                    print(f"[/ws/session] Session stopped — {len(frames)} frames buffered")

                    if not frames:
                        await websocket.send_json({
                            "type":     "final_result",
                            "status":   "error",
                            "feedback": "No frames received. Please try again.",
                        })
                        continue

                    session_data     = engine.process_session(frames)
                    summary          = engine.summarize_session(session_data)
                    summary["feedback"] = generate_feedback(summary)

                    print(f"[/ws/session] Result: reps={summary['rep_count']} score={summary['score']}")
                    await websocket.send_json(summary)

            except (json.JSONDecodeError, KeyError):
                if session_active:
                    frame = decode_frame(data)
                    if frame is not None:
                        frames.append(frame)

    except WebSocketDisconnect:
        print("[/ws/session] Client disconnected")
    except Exception as e:
        print(f"[/ws/session] Error: {e}")
